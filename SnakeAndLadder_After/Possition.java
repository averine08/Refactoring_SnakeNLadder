public class Possition {
    private int x;
    private int y;
    private int pos;

    public void setX(int X){
        this.x = X;
    }
    public void setY(int Y){
        this.y = Y;
    }
    public void setPos(int Pos){
        this.pos = Pos;
    }
    public int getX(){
        return this.x;
    }
    public int getY(){
        return this.y;
    }
    public int getPos(){
        return this.pos;
    }
}
